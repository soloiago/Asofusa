package com.iago.asofusa.utils;

public class Partido {
	private String id;
	private String partido;
	private String votado;
	private String resultadoPorra;
	private String resultadoReal;
	private String fecha;
	private String puntos;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPartido() {
		return partido;
	}
	public void setPartido(String partido) {
		this.partido = partido;
	}
	public String getVotado() {
		return votado;
	}
	public void setVotado(String votado) {
		this.votado = votado;
	}
	public String getResultadoPorra() {
		return resultadoPorra;
	}
	public void setResultadoPorra(String resultadoPorra) {
		this.resultadoPorra = resultadoPorra;
	}
	public String getResultadoReal() {
		return resultadoReal;
	}
	public void setResultadoReal(String resultadoReal) {
		this.resultadoReal = resultadoReal;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getPuntos() {
		return puntos;
	}
	public void setPuntos(String puntos) {
		this.puntos = puntos;
	}

}
